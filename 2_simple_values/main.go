package main

import "fmt"

func main() {
	// simple values
	fmt.Println(1)                // integer
	fmt.Println(3.14)             // float
	fmt.Println("Go is awesome!") // string
	fmt.Println(true)             // boolean
	fmt.Println(nil)              // nil
	fmt.Println('A')              // rune (character)
	fmt.Println([]int{1, 2, 3})   // slice (array)
}
